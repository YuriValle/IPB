#include <stdio.h>

int main() {
	int primeiroNumero = 5;
	int segundoNumero = 6;
	if (primeiroNumero > segundoNumero) {
		printf("O primeiro numero e maior do que o segundo numero.");
	}
	if (primeiroNumero < segundoNumero) {
		printf("O primeiro numero e menor do que o segundo numero.");
	}
	if (primeiroNumero == segundoNumero) {
		printf("O primeiro numero e igual ao segundo numero.");
	}
}
